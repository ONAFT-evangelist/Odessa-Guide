package com.example.georg.odessaguide.pojo;

public class Other {

    private String id;
    private String title;

    public Other(String id, String title) {
        this.id = id;
        this.title = title;
    }

    public Other() {

    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
